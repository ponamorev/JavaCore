package com.andersen.study.chap5;
// Демонстрация применения оператора цикла for
class ForTick {
    public static void main(String[] args) {
        // int n;
        // Оъявим переменную управления n в самом цикле
        for (int n = 10; n > 0; n--)
            System.out.println("такт " + n);
    }
}
